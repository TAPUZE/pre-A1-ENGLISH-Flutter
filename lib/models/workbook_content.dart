// Complete workbook content for PRE A1 English Learning Platform
// Based on "PRE A1 English Workbook: Your First Trip Abroad"

class WorkbookSection {
  final int id;
  final String title;
  final String titleHebrew;
  final String storyStep;
  final String storyStepHebrew;
  final List<Map<String, dynamic>> teachings;
  final List<Map<String, dynamic>> exercises;
  final List<Map<String, dynamic>> games;
  final List<Map<String, dynamic>> reviews;
  final Map<String, dynamic> mistakeLog;

  WorkbookSection({
    required this.id,
    required this.title,
    required this.titleHebrew,
    required this.storyStep,
    required this.storyStepHebrew,
    required this.teachings,
    required this.exercises,
    required this.games,
    required this.reviews,
    required this.mistakeLog,
  });

  // Getters for compatibility with widget code
  List<String> get vocabulary {
    List<String> vocabList = [];
    for (var teaching in teachings) {
      if (teaching['type'] == 'vocabulary' && teaching['items'] != null) {
        vocabList.addAll(List<String>.from(teaching['items']));
      }
    }
    return vocabList;
  }

  List<String> get grammar {
    List<String> grammarList = [];
    for (var teaching in teachings) {
      if (teaching['type'] == 'grammar' && teaching['items'] != null) {
        grammarList.addAll(List<String>.from(teaching['items']));
      }
    }
    return grammarList;
  }

  List<String> get stories {
    return [storyStep]; // Return the story step as a single-item list
  }

  factory WorkbookSection.fromMap(Map<String, dynamic> map) {
    return WorkbookSection(
      id: map['id'] ?? 0,
      title: map['title'] ?? '',
      titleHebrew: map['titleHebrew'] ?? '',
      storyStep: map['storyStep'] ?? '',
      storyStepHebrew: map['storyStepHebrew'] ?? '',
      teachings: List<Map<String, dynamic>>.from(map['teachings'] ?? []),
      exercises: List<Map<String, dynamic>>.from(map['exercises'] ?? []),
      games: List<Map<String, dynamic>>.from(map['games'] ?? []),
      reviews: List<Map<String, dynamic>>.from(map['reviews'] ?? []),
      mistakeLog: Map<String, dynamic>.from(map['mistakeLog'] ?? {}),
    );
  }
}

